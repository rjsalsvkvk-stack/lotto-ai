  //  반환: { loaded: Number, maxRound: Number, ageHours: Number }
  // ──────────────────────────────────────────────────────────
  function _loadCache() {
    try {
      const raw = localStorage.getItem(CACHE_KEY);
      if (!raw) return { loaded: 0, maxRound: BUILT_IN_MAX, ageHours: Infinity };

      const cached = JSON.parse(raw);
      let loaded = 0;
      for (const [r, d] of Object.entries(cached)) {
        if (d?.n?.length === 6) {
          _db[Number(r)] = d;
          loaded++;
        }
      }
      const ts  = localStorage.getItem(CACHE_TS_KEY);
      const age = ts ? (Date.now() - Number(ts)) / 3_600_000 : Infinity;
      const max = loaded ? Math.max(...Object.keys(_db).map(Number)) : BUILT_IN_MAX;
      return { loaded, maxRound: max, ageHours: age };
    } catch (e) {
      console.warn('[Collector] 캐시 로드 실패', e);
      return { loaded: 0, maxRound: BUILT_IN_MAX, ageHours: Infinity };
    }
  }

  // ──────────────────────────────────────────────────────────
  //  공개 API
  // ──────────────────────────────────────────────────────────
  async function init() {
    // 1) 내장 DB 복사
    _db = { ...BUILT_IN_DB };

    _emit('lotto:progress', { pct: 2, text: '내장 DB 초기화 중...', phase: 'init' });

    // 2) 캐시 로드
    const cache = _loadCache();

    if (cache.loaded > 0) {
      _emit('lotto:progress', {
        pct: 8,
        text: `캐시 ${cache.loaded}회차 로드 완료 → 최신 데이터 확인 중...`,
        phase: 'cache'
      });
      // 캐시 데이터로 즉시 UI 갱신 가능하도록 ready 임시 발행
      _emit('lotto:ready', {
        total:    Object.keys(_db).length,
        maxRound: cache.maxRound,
        newCount: cache.loaded,
        partial:  true
      });
    }

    // 3) 최신 회차 추정
    const estimated = _estimateLatest();
    const currentMax = cache.maxRound;

    console.log(`[Collector] 내장: ${BUILT_IN_MAX} | 캐시 최대: ${currentMax} | 예상 최신: ~${estimated}`);

    // 4) 캐시가 충분히 최신이면 수집 생략
    if (currentMax >= estimated && cache.ageHours < 1) {
      console.log('[Collector] 캐시 최신 → 수집 생략');
      _finalize();
      return;
    }

    // 5) 실제 수집 시작
    const fetchFrom = Math.max(currentMax + 1, BUILT_IN_MAX + 1);

    _emit('lotto:progress', {
      pct: 10,
      text: '동행복권 최신 회차 탐색 중...',
      phase: 'probe'
    });

    const trueLatest = await _findTrueLatest(estimated);

    if (trueLatest === null) {
      console.warn('[Collector] 최신 회차 탐색 실패 → 내장+캐시 데이터로 진행');
      _finalize();
      return;
    }

    await _fetchRange(fetchFrom, trueLatest);
    _saveCache();
    _finalize();
  }

  function _finalize() {
    const total    = Object.keys(_db).length;
    const maxRound = Math.max(...Object.keys(_db).map(Number));
    const newCount = total - Object.keys(BUILT_IN_DB).length;

    _emit('lotto:progress', { pct: 100, text: '동기화 완료', phase: 'done' });
    _emit('lotto:ready',    { total, maxRound, newCount, partial: false });
  }

  return {
    init,
    getDB:       () => ({ ..._db }),
    getMaxRound: () => Math.max(...Object.keys(_db).map(Number)),
    BUILT_IN_MAX
  };

