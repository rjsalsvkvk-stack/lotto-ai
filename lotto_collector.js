  //  내부: 누락 회차 일괄 수집 (fetchFrom ~ latestRound)
  // ──────────────────────────────────────────────────────────
  async function _fetchRange(fetchFrom, latestRound) {
    const missing = [];
    for (let r = latestRound; r >= fetchFrom; r--) {
      if (!_db[r]) missing.push(r);
    }
    if (!missing.length) return;

    let done = 0;
    const total = missing.length;

    for (const r of missing) {
      const data = await _fetchOne(r);
      if (data) _db[r] = data;
      done++;

      const pct = Math.round((done / total) * 85) + 10;
      _emit('lotto:progress', {
        pct,
        text: `수집 중... ${r}회 (${done}/${total})`,
        phase: 'fetch'
      });

      if (done % 15 === 0) await new Promise(r => setTimeout(r, 50));
    }
  }

