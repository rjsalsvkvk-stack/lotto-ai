  //  내부: 날짜 기반 예상 최신 회차 계산
  //  로또 1회: 2002-12-07 (토요일), 매주 토요일 추첨
  //  ★ 버그 수정: 실제 최신 회차는 1213+α이므로 BUILT_IN_MAX 이하는 반환 안 함
  // ──────────────────────────────────────────────────────────
  function _estimateLatest() {
    const FIRST = new Date(2002, 11, 7); // 2002-12-07
    const now   = new Date();
    const weeks = Math.floor((now - FIRST) / (7 * 24 * 60 * 60 * 1000));
    const est   = weeks + 1;
    // 계산 오차 대비: 내장 최대 회차보다 작으면 내장+1 반환
    return Math.max(est, BUILT_IN_MAX + 1);
  }

  // ──────────────────────────────────────────────────────────
  //  내부: 동행복권 API 1회차 수집
  //  ★ 버그 수정: 직접 호출 → 캐시 프록시 → 전체 프록시 순차 시도
  // ──────────────────────────────────────────────────────────
  async function _fetchOne(round) {
    const BASE = `https://www.dhlottery.co.kr/common.do?method=getLottoNumber&drwNo=${round}`;
    const TIMEOUT = 8000;

    const _parse = j => {
      if (j?.returnValue !== 'success') return null;
      return {
        n: [j.drwtNo1,j.drwtNo2,j.drwtNo3,j.drwtNo4,j.drwtNo5,j.drwtNo6].sort((a,b)=>a-b),
        b: j.bnusNo,
        d: j.drwNoDate ? j.drwNoDate.replace(/-/g, '.') : ''
      };
    };

    // 1) 직접 호출 (CORS 허용 환경)
    try {
      const r = await fetch(BASE, { signal: AbortSignal.timeout(5000) });
      const j = await r.json();
      const res = _parse(j);
      if (res) { _workingProxy = -1; return res; }
    } catch (_) { /* CORS 차단 → 프록시로 진행 */ }

    // 2) 마지막 성공 프록시 우선 시도
    if (_workingProxy >= 0) {
      try {
        const r = await fetch(PROXIES[_workingProxy](BASE), { signal: AbortSignal.timeout(TIMEOUT) });
        const j = JSON.parse(await r.text());
        const res = _parse(j);
        if (res) return res;
      } catch (_) { _workingProxy = -1; }
    }

    // 3) 모든 프록시 순차 시도
    for (let i = 0; i < PROXIES.length; i++) {
      try {
        const r = await fetch(PROXIES[i](BASE), { signal: AbortSignal.timeout(TIMEOUT) });
        const j = JSON.parse(await r.text());
        const res = _parse(j);
        if (res) { _workingProxy = i; return res; }
      } catch (_) { /* 다음 프록시 */ }
    }
    return null;
  }

