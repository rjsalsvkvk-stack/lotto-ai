  //  내부: localStorage 캐시 저장 (내장 DB 제외 신규분만)
  // ──────────────────────────────────────────────────────────
  function _saveCache() {
    try {
      const extra = {};
      for (const [r, d] of Object.entries(_db)) {
        if (!BUILT_IN_DB[r]) extra[r] = d;
      }
      if (Object.keys(extra).length) {
        localStorage.setItem(CACHE_KEY, JSON.stringify(extra));
        localStorage.setItem(CACHE_TS_KEY, Date.now().toString());
      }
    } catch (e) { console.warn('[Collector] 캐시 저장 실패', e); }
  }

